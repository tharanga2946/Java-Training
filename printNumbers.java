package com.Test;

public class printNumbers 
{
	public static void main(String[] args) 
	{
		for (int i = 0; i <= 10; i++) 
		{
			System.out.println(i);
		}
		System.out.println("========================================");

		System.out.println("Alphabet is:");
		for (char j = 'A'; j <= 'Z'; j++)
		{
			System.out.println(j);	
		}
	}
}
